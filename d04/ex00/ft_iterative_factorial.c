int	ft_iterative_factorial(int nb)
{
	int i;
	int rs;

	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	i = 2;
	rs = 1;
	while (i <= nb)
	{
		rs *= i;
		i++;
	}
	return (rs);
}
