int	ft_iterative_power(int nb, int power)
{
	int i;
	int rs;

	if (power < 0)
	{
		return (0);
	}
	i = 0;
	rs = 1;
	while (i < power)
	{
		rs *= nb;
		i++;
	}
	return (rs);
}
