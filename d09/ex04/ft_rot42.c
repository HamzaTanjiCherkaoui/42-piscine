char	char_change(char c)
{
	int		i;
	char	rs;

	i = 0;
	rs = c;
	if (!(c >= 'a' && c <= 'z') && !(c >= 'A' && c <= 'Z'))
		return (c);
	while (i < 42)
	{
		rs++;
		if (rs == 'z' + 1 || rs == 'Z' + 1)
			rs -= 'z' - 'a' + 1;
		i++;
	}
	return (rs);
}

char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		str[i] = char_change(str[i]);
		i++;
	}
	return (str);
}
