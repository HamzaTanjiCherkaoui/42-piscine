int	ft_compact(char **tab, int length)
{
	int		i;
	int		j;

	i = 0;
	while (i < length)
	{
		if (*(tab + i) != 0)
		{
			i++;
			continue ;
		}
		j = i;
		length--;
		while (j < length)
		{
			*(tab + j) = *(tab + j + 1);
			j++;
		}
	}
	return (length);
}
