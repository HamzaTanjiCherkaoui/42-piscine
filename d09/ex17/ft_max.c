int		ft_max(int *tab, int length)
{
	int i;
	int min;

	if (length == 0)
		return (0);
	i = 0;
	min = tab[0];
	while (i < length)
	{
		if (min > tab[i])
			min = tab[i];
		i++;
	}
	return (min);
}
